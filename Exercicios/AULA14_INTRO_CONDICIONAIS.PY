# if = se/ elif = se não se / else = se não
entrada = input("você quer 'entrar' ou 'sair' do sistema?")

if entrada == 'entrar':
    print("Bem vindo, você entrou no sistema!")
elif entrada == 'sair':
    print("Você saiu do sistema, até a proxima!")
else: 
    print("Você não digitou uma resposta válida")

